// возвращает ссылку на символ
text_t* get_symbol(char symbol) {
	if (is_between('0', symbol, '9')) { // число
		return digits[symbol - '0'];
	}
	else if (is_between('a', symbol, 'z')) { // строчная буква
		return small_letters[symbol - 'a'];
	}
	else if (is_between('A', symbol, 'Z')) { // заглавная буква
		return letters[symbol - 'A'];
	}
	else { // символы
		if (symbol == '-') { // -
			return symbols[0];
		}
		else if (symbol == '.') { // .
			return symbols[1];
		}
		else if (symbol == '!') { // !
			return symbols[2];
		}
		else if (symbol == '?') { // ?
			return symbols[3];
		}
		else {
			return unused_character[0];
		}
	}
}

// возвращает длину символа по x
s32 symbol_len(char symbol) {
	if (symbol == ' ') {
		return 4;
	}
	else {
		s32 right_x = -1;

		auto memory = get_symbol(symbol);
		for (u32 i = 0; i < 7; i++) {
			auto row = memory[i];
			// u32 j = 0?
			for (s32 j = 1; *row; j++, row++) {
				if (*row == '0' && right_x < j) {
					right_x = j;
				}
			}
		}
		return right_x + 1;
	}
}

// возвращает длину текста
s32 text_len(text_t text) {
	s32 len = 0;
	while (*text) {
		len += symbol_len(*text);
		text++;
	}
	return len;
}

s32 sprite_len(sprite_t sprite) {
	s32 right_x = -1;
	auto& pixels = Sprites[sprite].picture;

	for (u32 i = 0; i < pixels.getRowLen(); i++) {
		for (u32 j = 0; j < pixels.getColLen(); j++) {
			if (pixels[i][j].is_draw && right_x < j) {
				right_x = j;
			}
		}
	}

	return right_x + 2;
}
